
public class Arithmetic_Operators {
    
    public static void main(String[] args) {

        double firstNumber=2.5;
        double secondNumber=3.5;
        double result;
        
        result=firstNumber+secondNumber;
        System.out.println("results of addition is: " + result);

        result=firstNumber-secondNumber;
        System.out.println("result of subtraction is: "+ result);

        result=firstNumber/secondNumber;
        System.out.println("result of devision is: "+ result);

        result=firstNumber*secondNumber;
        System.out.println("result of multiplication is: "+ result);
    
    }
}
